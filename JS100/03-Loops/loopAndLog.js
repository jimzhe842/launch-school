for (let i = 0; i <= 10; i += 2) {
  console.log(i)
}

// logs 0,2,4,6,8,10
// the log 0 to 10 is inclusive
// loop 1: 0 // when the loop is initialized, it checks to see if initial variable declaration meets the condition, and if so, it executes the loop statement
// loop 2: 2 // after the first loop, the update expression (i += 2) is executed
// loop 3: 4
// loop 4: 6
// loop 5: 8
// loop 6: 10 // stops looping when the condition (i <= 10) is false